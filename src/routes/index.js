// -----------------------------------------------------------------------------
// Initalise Express Router
// -----------------------------------------------------------------------------
const express = require('express');

const router = express.Router();

// -----------------------------------------------------------------------------
// Require in Controllers
// -----------------------------------------------------------------------------
const home = require('./controllers/home.js');

// -----------------------------------------------------------------------------
// Routes
// -----------------------------------------------------------------------------
router
  .route('/')
  .get(home.renderHome);

// -----------------------------------------------------------------------------
// Export Router
// -----------------------------------------------------------------------------
module.exports = router;
